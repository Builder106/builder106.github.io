// Build-time script: pulls star count, primary language, and last-push
// timestamp for each project's GitHub repo and writes the result to
// `src/data/repoStats.generated.ts`. The generated module is committed
// to the tree as a safe fallback so the site still builds offline / on
// rate-limit failure; the prebuild step refreshes it when `gh` is
// available.
//
// Hits `gh api` (works without an explicit token in CI because the
// runner ships an authenticated GITHUB_TOKEN). On local machines, the
// user's gh CLI auth is reused.

import { execSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");
const projectsPath = resolve(repoRoot, "src/data/projects.ts");
const outPath = resolve(repoRoot, "src/data/repoStats.generated.ts");

// Crude extraction — the projects file is a plain TS literal, so a
// regex over `repo: "https://github.com/<owner>/<name>"` is sufficient.
function extractRepoSlugs() {
  const src = readFileSync(projectsPath, "utf8");
  const re = /repo:\s*"https:\/\/github\.com\/([^"]+)"/g;
  const slugs = new Set();
  for (const m of src.matchAll(re)) slugs.add(m[1]);
  return [...slugs];
}

function fetchOne(slug) {
  try {
    const out = execSync(
      `gh api repos/${slug} --jq '{stars: .stargazers_count, lang: .language, pushed_at: .pushed_at}'`,
      { encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] },
    );
    return JSON.parse(out);
  } catch (err) {
    console.warn(`[repo-stats] ${slug}: ${err.message?.split("\n")[0]}`);
    return null;
  }
}

const slugs = extractRepoSlugs();
const stats = {};
for (const slug of slugs) {
  const data = fetchOne(slug);
  if (data) stats[slug] = data;
}

// If every fetch failed (no gh, no network, no token), don't overwrite
// the committed file with an empty map — that would erase the badges
// from the live site. The previously-generated file becomes the
// fallback and the build still succeeds.
if (Object.keys(stats).length === 0) {
  console.warn(
    `[repo-stats] no entries fetched — keeping existing ${outPath} as fallback`,
  );
  process.exit(0);
}

const header =
  "// Auto-generated by scripts/fetch-repo-stats.mjs at build time.\n" +
  "// Don't edit by hand — regenerate via `npm run build`.\n\n";

const body = `export interface RepoStats {
  stars: number;
  lang: string | null;
  pushed_at: string;
}

export const repoStats: Record<string, RepoStats> = ${JSON.stringify(stats, null, 2)};
`;

writeFileSync(outPath, header + body);
console.log(`[repo-stats] wrote ${Object.keys(stats).length} entries → ${outPath}`);
